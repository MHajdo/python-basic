# Mood checker

mood = input("What mood are you in: ")

if mood == "happy":
    print('It is great to see you happy!')
elif mood == "nervous":
    print('Take a deep breath 3 times.')
elif mood == "sad":
    print('Cheer up')
elif mood == "excited":
    print('Woohooo!')
elif mood == "relaxed":
    print('keep chilling')
else:
    print("I don't recognize this mood")






